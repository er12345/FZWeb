drop table if exists fbSol;

CREATE TABLE fbsol
(
   runID varchar(255),
   solTimeID varchar(255),
   solType varchar(5),
   restan double,
   lastJobEndTime int,
   lastJobEndClock varchar(5),
   vehicles int,
