;

drop table if exists fbTask;

CREATE TABLE fbtask
(
   runID varchar(255),
   solTimeID varchar(255),
   clock1 varchar(255),
   agnID varchar(255),
   splyID varchar(255),
   blocks varchar(255),
   size1 double,
   startMin int,
   arrvSplyMin int,
   waitSplyDur int,
   dprtSplyMin int,
   arrvMillMin int,
   endMin int,
   lonSply double,
   latSply double,
   lonMill double,
   latMill double,
   millName varchar(255),
   lastUpd timestamp DEFAULT CURRENT_TIMESTAMP  ON UPDATE CURRENT_TIMESTAMP,
   status varchar(255)
