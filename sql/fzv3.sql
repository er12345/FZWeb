drop table if exists fbDefinedPath;

CREATE TABLE fbdefinedpath
(
   fromID varchar(255),
   toID varchar(255),
   seq int,
   roadID varchar(255),
   segmentSeq int,
   x1 double,
   y1 double,
   x2 double,
   y2 double,
   distMtr double
)
;

drop table if exists fbDiv;

CREATE TABLE fbdiv
(
   divID varchar(255),
   estateID varchar(255),
   millID varchar(255)
