        TaskID int not null AUTO_INCREMENT
        , JobID int
        , From1 varchar(255)
        , To1 varchar(255)
        , PlanStart datetime
        , PlanEnd datetime
        , ActualStart datetime
        , ActualEnd datetime
        , DoneStatus varchar(5) 
        , FromDesc varchar(255)
        , ToDesc varchar(255)
        , Tonnage float
        , Blocks varchar(255)
        , TaskSeq INT
        , TaskType varchar(5) 
        , PhaseType varchar(5) 
        , takenDt timestamp 
        , doneDt timestamp 
        , createDt timestamp DEFAULT CURRENT_TIMESTAMP
        , updDt timestamp DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP  
        , reasonState int
        , reasonID int
        , primary key (TaskID)
